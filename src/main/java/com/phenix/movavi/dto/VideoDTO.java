package com.phenix.movavi.dto;

/**
 * Informations vidéos.
 *
 * @author <a href="mailto:edouard128@hotmail.com">Edouard Jeanjean</a>
 */
public class VideoDTO {

    /**
     * Aspect du pixel horizontalement.
     */
    public int aspectX;

    /**
     * Aspect du pixel verticalement.
     */
    public int aspectY;

    /**
     * Dénominateur de la valeur du framerate.
     */
    public int frameRateD;

    /**
     * Numérateur de la valeur du framerate.
     */
    public int frameRateN;

    /**
     * Hauteur de l'image.
     */
    public int height;

    /**
     * Largeur de l'image.
     */
    public int width;
}
